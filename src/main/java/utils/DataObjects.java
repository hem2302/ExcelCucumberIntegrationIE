package utils;

public class DataObjects {
	public String uname= "";
	public String pwd = "";
	
	public void setUname(String uname){
		this.uname= uname;
	}
	public void setPWD(String pwd) {
		this.pwd = pwd;
	}
	public String getUname() {
		return this.uname;
	}
	public String getPWD() {
		return this.pwd;
	}
}
